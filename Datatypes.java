class Datatypes

{
public static void main (String[] args)
	{
	boolean b=true;
	int i=10;
	char c='V';
	byte w=2;
	short s=5;
	long l=123345L;
	float f=42.35f;
	System.out.println("Boolean: "+b);
	System.out.println(i);	
	System.out.println(c);
	System.out.println(w);
	System.out.println(s);	
	System.out.println(l);
	System.out.println(f);	
}

}